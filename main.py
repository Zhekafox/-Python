# Посчитать в веденной строке процент сожердания символов "G","C" (регистр не должен влиять)

genome=input()
str=genome.upper() # приводим к единому регистру
cnt=0 # счетчик, куда будем записывать кол-во совпадений
for nucl in str: # цикл перебора символов
    if nucl=='G' or nucl=='C': # ищем символы
        cnt+=1 # добавляем к счетчику
print(float((cnt/len(genome))*100))

