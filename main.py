# кодирование строки 'aaaabbcaa' в 'a4b2c1a2'

stroka=input() # ввод строки
y=0 # в python 1-ый символ в строке 0 (нулевой)
s=1 # счетчик = 1, т.к. если 1 символ в строке
for i in stroka: # цикл перебора символов
    if y<(len(stroka)-1) and stroka[y]==stroka[y+1]: # сравнение 0-го и 1-го символа, а так же учитываем,
        s+=1                                         # что строка начинаеться с 0-го (python)
        y+=1                                         # счетчик+1 и y+1 переход к следующему символу
    elif y<(len(stroka)-1) and stroka[y]!=stroka[y+1]: # если 0-ый и 1-ый символ !=, а так же учитываем,
        print(i+str(s), end='')                      # что строка начинаеться с 0-го (python), далее вывод
        s=1                                          # символа+суммы букв, а так end='', для вывода в строку без пробела
        y+=1                                         # счетчик+1 и y+1 переход к следующему символу
print(i+str(s), end='') # когда y=(кол-во букв-1) то выводит текущее значение буквы+кол-во данных букв