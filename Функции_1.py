# Функция не должна ничего возвращать, требуется только изменение переданного списка

lst = [1,2,3,4]
def modify_list(l): # ВАЖНО, в функции список явно не пишиться, а обращаеться через функции
   i=(len(l)-1) # проверяем список в обратном порядке, определяем кол-во символов в списке
   while i>=0: # цикл проверки символов,= т.к. нужно проверить 0-ой символ 
       if l[i]%2==0: # если число /2 без остатка, то
           l[i]=(l[i]//2) # число в списке делим на 2
       else:
           l.remove(l[i]) # иначе удаляем
       i-=1 # счетчик уменьшаем на 1
modify_list(lst) # вызов функции
