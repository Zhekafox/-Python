    res=[] # создаем пустой список
    if step>0: # условие истено
        x=start # x = 1-ый элемент списка
        while x<stop: # цикл, элемент < последнего элемента
            res+=[x] # добавляем элемент в список
            x+=step # элемент увеличиваем на 1-н шаг
    elif step<0: # условие истено
        x=start # x = 1-ый элемент списка
